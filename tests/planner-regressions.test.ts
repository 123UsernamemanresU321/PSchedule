test("paired paper reviews retain their dependency scheduling window", () => {
  const dataset = buildSeedDataset(new Date("2026-03-13T08:00:00"));
  const reviewTopic = dataset.topics.find(
    (topic) => topic.id === "physics-past-papers-week-8-paper-2-review",
  );

  assert.ok(reviewTopic, "expected seeded review topic");
  assert.equal(reviewTopic.maxDaysAfterDependency, 7);

  const existingPracticeBlock = createStudyBlock({
    id: "practice-1",
    weekStart: "2026-09-21",
    date: "2026-09-22",
    start: "2026-09-22T08:00:00.000Z",
    end: "2026-09-22T10:30:00.000Z",
    topicId: "physics-past-papers-week-8-paper-2",
    title: "Physics HL Week 8 - Paper 2",
    estimatedMinutes: 150,
  });

  const candidates = buildTaskCandidates({
    topics: [reviewTopic as Topic],
    existingPlannedBlocks: [existingPracticeBlock],
    referenceDate: new Date("2026-09-21T08:00:00"),
    subjectDeadlinesById: { "physics-hl": "2027-06-30" },
  });
  const reviewCandidate = candidates.find((candidate) => candidate.topicId === reviewTopic.id);

  assert.ok(reviewCandidate, "expected a schedulable review candidate");
  assert.equal(reviewCandidate?.paperCode, "PHY-W08-P2");
  assert.equal(reviewCandidate?.availableAt?.slice(0, 10), "2026-09-23");
  assert.equal(reviewCandidate?.latestAt?.slice(0, 10), "2026-09-29");
});

test("calendar completion forecast distinguishes impossible from simply underplanned", () => {
  const dataset = buildSeedDataset(new Date("2026-03-13T08:00:00"));
  const subject = dataset.subjects.find((candidate) => candidate.id === "physics-hl");
  assert.ok(subject, "expected physics subject");

  const underplannedForecast = getCalendarCompletionForecast({
    subject,
    topics: dataset.topics,
    goals: dataset.goals,
    studyBlocks: [],
    weeklyPlans: [createWeeklyPlan()],
    referenceDate: new Date("2026-03-13T08:00:00"),
  });

  assert.equal(underplannedForecast.isCalendarImpossible, false);
  assert.equal(underplannedForecast.needsMoreBlocks, true);

  const impossibleForecast = getCalendarCompletionForecast({
    subject,
    topics: dataset.topics,
    goals: dataset.goals,
    studyBlocks: [],
    weeklyPlans: [createWeeklyPlan({ slackMinutes: 0 })],
    referenceDate: new Date("2026-03-13T08:00:00"),
  });

  assert.equal(impossibleForecast.isCalendarImpossible, true);
});

test("regeneration preserves user-touched future blocks", () => {
  assert.equal(
    shouldPreserveStudyBlockOnRegeneration(
      createStudyBlock({
        isAutoGenerated: false,
      }),
    ),
    true,
  );

  assert.equal(
    shouldPreserveStudyBlockOnRegeneration(
      createStudyBlock({
        notes: "Keep this slot for tutoring notes",
      }),
    ),
    true,
  );

  assert.equal(
    shouldPreserveStudyBlockOnRegeneration(
      createStudyBlock({
        rescheduleCount: 1,
      }),
    ),
    true,
  );

  assert.equal(
    shouldPreserveStudyBlockOnRegeneration(
      createStudyBlock({
        status: "planned",
        notes: "",
        rescheduleCount: 0,
        isAutoGenerated: true,
      }),
    ),
    false,
  );
});

test("generated horizon passes core validation checks for a short seeded window", () => {
  const referenceDate = new Date("2026-03-13T08:00:00");
  const dataset = buildSeedDataset(referenceDate);
  const result = generateStudyPlanHorizon({
    startWeek: new Date("2026-03-09T00:00:00"),
    endWeek: new Date("2026-03-30T00:00:00"),
    goals: dataset.goals,
    subjects: dataset.subjects,
    topics: dataset.topics,
    fixedEvents: dataset.fixedEvents,
    preferences: dataset.preferences,
  });

  const issues = validateGeneratedHorizon({
    studyBlocks: result.studyBlocks,
    topics: dataset.topics,
    weeklyPlans: result.weeklyPlans,
  });

  const errorIssues = issues.filter((issue) => issue.severity === "error");
  assert.deepEqual(
    errorIssues,
    [],
    `expected no blocking validation issues, got: ${errorIssues.map((issue) => issue.message).join(" | ")}`,
  );
});
